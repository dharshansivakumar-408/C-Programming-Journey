// Swapped numbers without using a temp variable!
#include"stdio.h"
int main()
{
    int p,q;
    printf("Enter P value:");
    scanf("%d",&p);
    printf("Enter Q value:");
    scanf("%d",&q);
    printf("After swapping");
    printf("\nEnter P value:%d",q);
    printf("\nEnter Q value:%d",p);
    
}
